package chapter08;

public class MyDate {
	
	// 멤버변수
	private int day;
	private int month;
	private int year;
	
	// 디폴드 생성자(생략)
	
	// 메서드 
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
}
