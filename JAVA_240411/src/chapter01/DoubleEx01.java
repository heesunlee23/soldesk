package chapter01;

public class DoubleEx01 {

	public static void main(String[] args) {
		// 정수형: byte -> short -> int -> long
		// 문자형: char('') -> String("")
		// 실수형: float -> double
		
		char a = 'a';
		float fNum = 3.14f;
		// f 없을 때: 왜 빨간색 밑줄? 
		// 실수형의 기본은  double 임
		// f 추가 
		
		double dNum = 3.14;
		
		System.out.println(fNum);
		System.out.println(dNum);
		
	}

}
