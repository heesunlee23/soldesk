package chapter01;

public class CharacterEx3 {

	public static void main(String[] args) {
		
		int a = 65; // 아스키코드 대문자 A(65), 소문자 a(97)
		System.out.println(a);
		System.out.println((char)a);
		
		// 숫자 -> 문자 
		// 65  -> A
		
		int b = 66;
		System.out.println(b);
		System.out.println((char)b);
	}

}
